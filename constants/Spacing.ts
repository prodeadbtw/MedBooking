// Отступы — это расстояния между элементами.
// Мы используем шкалу с множителем 4 — это стандарт в дизайне.
// 4, 8, 12, 16, 20, 24, 32 — все числа кратны 4.
// Это создаёт визуальную гармонию в интерфейсе.

const Spacing = {
  xs: 4,    // Минимальный отступ
  sm: 8,    // Маленький
  md: 12,   // Средний
  lg: 16,   // Стандартный (самый используемый)
  xl: 20,   // Увеличенный
  xxl: 24,  // Большой
  xxxl: 32, // Очень большой

  // Скругление углов
  borderRadius: {
    sm: 8,   // Маленькое скругление (кнопки)
    md: 12,  // Среднее (карточки)
    lg: 16,  // Большое (модальные окна)
    full: 999, // Полный круг (аватарки)
  },
};

export default Spacing;